export function isVideo(file) {
  var ext = getExtension(file.type);
  switch (ext.toLowerCase()) {
    case "video/m4v":
    case "video/avi":
    case "video/mpg":
    case "video/mp4":
      return true;
  }
  return false;
}
function getExtension(filename) {
  var parts = filename.split(".");
  return parts[parts.length - 1];
}
